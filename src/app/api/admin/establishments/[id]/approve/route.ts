import { NextRequest, NextResponse } from 'next/server'
import { getServerSession } from 'next-auth'
import { authOptions } from '@/lib/auth'
import { prisma } from '@/lib/prisma'
import { sendRestaurantApproval } from '@/lib/email/restaurant-verification'

// POST - Aprobar restaurante (solo admin)
export async function POST(
  request: NextRequest,
  { params }: { params: { id: string } }
) {
  try {
    console.log('‚úÖ [Admin] Approving establishment:', params.id)
    const session = await getServerSession(authOptions)

    if (!session || session.user.role !== 'ADMIN') {
      console.log('‚ùå [Admin] Unauthorized access attempt')
      return NextResponse.json(
        { success: false, message: 'No autorizado' },
        { status: 401 }
      )
    }

    const establishment = await prisma.establishment.findUnique({
      where: { id: params.id },
      include: {
        user: {
          select: {
            id: true,
            email: true,
            name: true,
          },
        },
      },
    })

    if (!establishment) {
      console.log('‚ùå [Admin] Establishment not found')
      return NextResponse.json(
        { success: false, message: 'Restaurante no encontrado' },
        { status: 404 }
      )
    }

    // Aprobar restaurante
    const updated = await prisma.establishment.update({
      where: { id: params.id },
      data: { 
        isActive: true,
        // @ts-ignore - verificationStatus will be added after migration
        verificationStatus: 'APPROVED',
        // @ts-ignore - approvedAt will be added after migration
        approvedAt: new Date(),
        // @ts-ignore - approvedBy will be added after migration
        approvedBy: session.user.id,
      } as any,
    })

    console.log(`‚úÖ [Admin] Establishment approved: ${establishment.name}`)

    // Enviar email de aprobaci√≥n
    try {
      await sendRestaurantApproval(updated as any, establishment.user as any)
      console.log('üìß [Admin] Approval email sent')
    } catch (emailError) {
      console.error('‚ö†Ô∏è [Admin] Failed to send email:', emailError)
      // Don't fail the request if email fails
    }

    return NextResponse.json({
      success: true,
      data: updated,
      message: `Restaurante "${establishment.name}" aprobado exitosamente`,
    })
  } catch (error) {
    console.error('‚ùå [Admin] Error approving establishment:', error)
    return NextResponse.json(
      { success: false, message: 'Error al aprobar restaurante' },
      { status: 500 }
    )
  }
}
